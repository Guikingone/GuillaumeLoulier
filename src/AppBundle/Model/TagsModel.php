namespace AppBundle\Model;

/**
 * Class TagsModel
 *
 * @author Guillaume Loulier <contact@guillaumeloulier.fr>
 */
abstract class TagsModel
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $title;

    /**
     * @return int
     */
    public function getId() : int
    {
        return $this->id;
    }

    /**
     * @return string
     */
    public function getTitle() : string
    {
        return $this->title;
    }

    /**
     * @param string $title
     */
    public function setTitle(string $title)
    {
        $this->title = $title;
    }
}
